# String
print('Avinash')  # will print Avinash

# print('Avinash's laptop') this will not work so we need to use double quotes
print("Avinash's laptop")

# print('Avinash's "loptop"') this will not work so we need to use the \ to ignore the default functionality
print('Avinash\'s "laptop"')

# String concatinating
print('Avi' + 'nash')

# IF you want to concat a string N number of times
print('Avi' * 10)

# How to print string in raw format
# will "r" will ignore the properties of "\n" here "\n" means new line
print(r'c:\n\avi')
