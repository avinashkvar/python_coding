x = 1  # variable assignment

print(x)  # will print 1

# Indexing   A  v  i  n  a  s  h
#            0  1  2  3  4  5  6  positive indexing
#           -7 -6 -5 -4 -3 -2 -1  negative indexing
name = 'Avinash'
print(name[0])  # will print first character
print(name[-1])  # will print the last character

# Sub string

print(name[0:4])  # will print from 0 - 3 index i.e. Avin
print(name[1:])  # will print from 1 - 6 index i.e vinash
print(name[:4])  # will print from 0 - 3 index i.e Avin

name = 'Shashank'
